// round() value returns the rounded number
console.log(Math.round(2.55));
// floor() method returns the lower bound value for the given decimal number.
console.log(Math.floor(2.55));
// ceil() method returns the upper value for the given decimal number.
console.log(Math.ceil(5.555));
//sqrt() method returns the square root of the given number.
console.log(Math.sqrt(169));
//abs() method returns positive value.
console.log(Math.abs(-299));
// random() method

console.log(Math.floor(Math.random()*1000));

// Math.trunc() returns the integer part of a number only

